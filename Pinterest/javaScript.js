const filterImages = (searchTerm) => {
  const lowerCaseSearchTerm = searchTerm.toLowerCase();
  const filteredImages = imageData.filter((data) => {
    return (
      data.title.toLowerCase().includes(lowerCaseSearchTerm) ||
      data.suggestedCategory.toLowerCase().includes(lowerCaseSearchTerm) ||
      data.profileName.toLowerCase().includes(lowerCaseSearchTerm)
    );
  });
  createImageCards(filteredImages);
};
document.getElementById("search-input").addEventListener("input", (event) => {
  const searchTerm = event.target.value;
  console.log('Search term:', searchTerm);
  filterImages(searchTerm);
});
